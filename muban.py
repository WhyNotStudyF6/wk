# ===============【导入库】===============
import tkinter                    # 导入tkinter库
import chuangjian as cj          # 导入创建包，简称/别名 为cj

# ===============【基础设置】===============
# 在这里修改你的基本配置


# ===============【创建窗口】===============
# 1. 创建主窗口
窗口标题 = "简单计算器 v1.0"        # 会出现在窗口顶部例如："简单计算器 v1.0"
窗口宽度 = 800                     # 改成你需要的宽度
窗口高度 = 600                     # 改成你需要的高度

主窗口 = cj.chuangjian_chuangkou(          # 创建一个窗口，窗口名为"主窗口"，可取其他名字，本质是对象/变量
    chuangkou_mingcheng=窗口标题,
    kuan=窗口宽度,
    gao=窗口高度
)

# ===============【添加控件】===============
# 2. 创建标签
cj.chuangjian_biaoqian(
    主窗口,                       # 标签所在的窗口（名），
    wenzi="请输入：",          # 例如："请输入："
    x=100,                        # 文本与窗口的左边距，单位：px（像素）
    y=100                         # 文本与窗口的上边距，单位：px（像素）
)

# 3. 创建文本框
文本框 = tkinter.Text(
    主窗口,                       # 文本框所在的窗口（名）
    width=40,                    # 文本框宽度（英文字符个数，1个汉字相当于2个英文字符）
    height=10                    # 文本框高度（行数）
)
文本框.place(x=10, y=50)         # 放置文本框的位置

# 4. 创建按钮和其功能
def 按钮被点击():
    """定义按钮被点击时要做的事"""
    tkinter.messagebox.showinfo(
        "提示",                   # 消息框标题
        "你点击了按钮！"           # 消息框内容
    )

cj.chuangjian_anni(
    主窗口,                       # 按钮所在的窗口（名）
    wenzi="点击我",               # 例如："点击我"
    mingling=按钮被点击,          # 点击时执行的函数
    x=10,                        # 按钮横向位置
    y=200                        # 按钮纵向位置
)

# ===============【显示窗口】===============
# 这一行永远要放在最后
主窗口.mainloop()

